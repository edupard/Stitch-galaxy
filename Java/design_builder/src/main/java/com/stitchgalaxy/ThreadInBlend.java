package com.stitchgalaxy;

public class ThreadInBlend {
	
	private Thread thread;
	
	private short flossCount;
	
	public ThreadInBlend()
	{
	}

	public Thread getThread() {
		return thread;
	}

	public void setThread(Thread thread) {
		this.thread = thread;
	}

	public short getFlossCount() {
		return flossCount;
	}

	public void setFlossCount(short flossCount) {
		this.flossCount = flossCount;
	}

}
